				<br>
				<br>

				The central location of IJCAI-16 will afford conference attendees accessibility to all that New York
				has to offer! Manhattan's extraordinary cityscape provides a dramatic charge for people of all
				interests and ages. Powerfully compact at 13.5 miles long, the borough is filled with soaring
				skyscrapers, glamorous neighborhoods, shops and restaurants, with the magnificent 843-acre
				<b>Central Park</b> - a National Historic Landmark - at its heart. Before or after an event, groups
				can sample Manhattan's iconic cultural masterpieces - <b>The Metropolitan Museum of Art, Museum of
				Modern Art and the Guggenheim Museum </b> - or experience a live show on <b>Broadway </b>, at Lincoln Center,
				a TV studio or in jazz clubs from Harlem to Greenwich Village. The list of key landmarks is topped
				by the <b> Statue of Liberty, the Empire State Building, Times Square, South Street Seaport,
				The Cloisters and Rockefeller Center</b>. Whether one chooses to shop, dine or just people-watch,
				there's truly no place like Manhattan.
