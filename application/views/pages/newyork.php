				Travel Tips
				<br>
				
				<i>Airports / Accessibility</i>
				<br>
				Getting to New York City from anywhere is quick and affordable. With three major airports and a combined total of over 20,000 flights per week, plus a comprehensive East Coast transit system, domestic and international attendees can get to NYC with ease.

				<br>
				Airports distance from Midtown:
				<br>

				<b>La Guardia</b> (8 miles)
				<br>
				<b>JFK</b> (15 miles)
				<br>
				<b>Newark</b> (16 miles)
				<br>
				<i>Airport Transportation</i>
				<br>
				<b>AirTrain JFK</b> is the easiest way to travel to, from and around Kennedy International Airport. AirTrain connects with MTA New York City Transit subways and buses and the Long Island Rail Road, providing a low-cost way to travel by mass transit to major destinations in New York and New Jersey. AirTrain makes frequent stops everywhere around the airport: airline terminals, parking lots, hotel shuttle areas and rental car facilities. AirTrain rides around the airport are free. The service operates 24 hours a day, 365 days a year.

				<br>
				<br>
				<b>Destination:</b> Midtown Manhattan, New York Penn Station
				<br>
				<b>Connection:</b> MTA NYC Transit E Subway at Jamaica Station
				<br>
				<b>Cost / Time:</b> $7.50 / 50 minutes
				<br>
				Trains from <b>Newark Airport</b> to Penn Station are $12.50 one way / 25 minutes
				<br>
