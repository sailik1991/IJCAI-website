				<br>
				<i>Trains / Accessibility </i>
				<br>
				<b>Amtrak:</b> America's national railroad operates several lines out of New York Penn Station, including the high-speed Acela. Penn Station is located under Madison Square Garden on West 34th Street, right in the heart of Midtown Manhattan. Amtrak offers an average of 85 daily trips into NYC from 200 destinations.

				<br>
				<br>
				<b>Commuter Transportation:</b> There are four local commuter trains in addition to an array of bus lines that provide easy access to NYC from the tristate area. NJ Transit, the Long Island Rail Road, Metro-North Railroad and the PATH (Port Authority Trans-Hudson) all run directly into NYC's two main rail stations, Penn Station and Grand Central Terminal.
				<br>
				<br>
				Ground Transportation
				<br>
				Subway System: MTA public subways remain the cheapest and quickest way to travel around the five boroughs. Operating in all five boroughs and carrying 7 million riders daily, the subway-the largest system in the world-is the heart of city transit. Trains operate 24/7. The cost of a single ride is $2.75; unlimited pass options are also available.
				<br>

				<br><b>Bus System:</b> MTA public buses can be accessed from almost every other city block and also operate 24 hours. Public buses are a scenic way to see the City and reach destinations not convenient to a subway stop.

				<br>
				<br>
				<b>Taxicabs:</b> Cabs can be hailed in any location and will drop you off exactly where you need to go. There is a minimum meter fare of $3, with price increases based on a set time-cost formula based on the time of day. All taxis accept cash and credit cards for payment.
		

