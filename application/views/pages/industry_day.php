The IJCAI Industry Day is a unique networking event where entrepreneurs and scientists discuss the future of the industrial application of AI. It leverages the unique environment of the oldest and largest AI conference, where 1300 scientists from top universities and research labs worldwide gather to discuss the technical state-of-the-art in Artificial Intelligence. 

<br><br>
Companies and entrepreneurs will find that the Industry Day is an excellent channel of communication with key researchers in every area of AI. 

<br><br>
The 2016 IJCAI Industry Day has a varied format, but every event encourages interaction and discussion about the business viability of AI technologies. 
<br>

<br> • Talks from industry executives about opportunities and strategies in key AI technologies in the next 5 years, including Cognitive Systems, Deep Learning, Computer Vision, Natural Language Processing and Robotics. 

<br> <br>• Panels where researchers in industrial labs and entrepreneurs discuss the process and challenges of bringing new AI technologies to the market.  

<br><br> • Poster session and micro career fair. Young talent and entrepreneurs are encouraged to share their work through posters and demos. 

<br>
<br>

<strong><em> About the participants.</em> </strong> It’s the biggest AI conference in existence, with 1300 scientists from all over the world. Depending on the edition of the conference, registrants consist of about 80%-90% academia and 10%-20% industry. Roughly half of them are students and junior researchers. 

