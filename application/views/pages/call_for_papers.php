	<h1> IJCAI-2016 Call for papers </h1>
</div>

        
The Program Committee of the Twenty-fifth International Joint Conference on Artificial Intelligence (IJCAI-16) invites the submission of original technical papers for the conference, to be held in New York, USA, from July 9th to July 16th, 2016. Submissions are invited on significant, original, and previously unpublished research on all aspects of artificial intelligence. For nearly a half-century, IJCAI has remained the premier conference bringing together the international AI community in communicating the advances  and celebrating the achievements  of artificial intelligence research and practice. With the current explosive interest in AI and its applications,  this 25th edition of the conference is guaranteed to provide an exciting forum to present and hear about the cutting-edge research in AI. 

<br><br> 
<strong> Topics </strong>
<br><br>

As the premier general conference on AI, IJCAI 2016 welcomes submissions across all areas of AI. The conference scope includes all subareas of AI, including (but not limited to) traditional topics such as search, machine learning, planning, knowledge representation, reasoning, natural language processing, robotics and perception, and multiagent systems. We expressly encourage work that cuts across technical areas and/or integrated capabilities. We encourage  all types of contributions including theoretical, engineering and applied. We also encourage  papers on AI techniques in the context of novel  application domains, such as security, sustainability, health care, transportation, and commerce.
A special theme of IJCAI-16 is Human-Aware AI. This theme will highlight the many beneficial ways AI technologies work hand-in-hand with humans. Papers relevant to this special theme can also be submitted through the main track submission site.  In addition to the main track, IJCAI 2016 will have a special track for submissions related to AI and the Web; see the special track call for additional details on that track. 

