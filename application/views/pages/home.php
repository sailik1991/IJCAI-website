          <h1> 
          Welcome to the 25th International Joint Conference on Artificial Intelligence IJCAI-16!
          </h1>
        </div>

        <p> 
        <br>
        We are delighted to invite you to come to New York, one of the most exciting cities of the world, and take part in IJCAI, the leading conference on the thrilling field of Artificial Intelligence. AI today has a tremendous impact. It is in all the media and makes a real difference. At IJCAI-16, you will have the opportunity to meet some of the world's leading AI researchers, to learn first-hand about their newest research results and developments, and to catch up with current AI trends in science and industry. And, of course, IJCAI-16 will be the perfect forum for presenting your own achievements, both to specialists in your field, and to the AI world in general.
        <br><br>
        The conference will include workshops, tutorials, exhibitions, demonstrations, invited talks, and paper/poster presentations. Exhibitions will be open to the public on Friday. Also on Friday there will be an Industry Day, with presentations from the top AI companies and a job fair; and there will be an AI Festival, open to the public, consisting of the IJCAI award winner's talks. You will not want to miss out on this highlight, so plan to stay at IJCAI-16 until the very end.
        <br><br>
        The conference (including workshops and tutorials) will be held at the Hilton in midtown Manhattan. The Hilton is just a ten-minute walk from Central Park, where the conference banquet will be held, and half a block from the world-famous Museum of Modern Art, where we will hold the opening reception. Times Square, the Broadway theater district, the Empire State Building and many other famous tourist attractions are all in walking distance --- an extra reason to join us and to add a couple of days to your visit.
        <br><br>
        So, mark the dates on your calendar and start thinking about which papers to submit and which systems to demonstrate. If you work for a company consider taking advantage of the exhibition opportunities IJCAI-16 will provide. And if you are a potential sponsor please check our sponsorship brochure. The visibility you will get by sponsoring IJCAI-16 will be hard to match.
        <br><br>Looking forward to meeting you in New York!
        <br>The IJCAI-16 Conference Committee.
        </p>
