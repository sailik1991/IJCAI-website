  <h1> IJCAI 2016  Special track on AI & The Web </h1>
</div>

  <p>
  
The AI and the Web Track of IJCAI 2016 invites research papers at the intersection of artificial intelligence and the World Wide Web. This includes research papers where the Web is used as a platform for application of AI techniques or concepts. This also includes papers that describe AI systems or technology, in which Web resources, or knowledge derived from the Web are an integral component. Submissions are expected to offer technical novelty beyond straightforward applications of existing AI techniques to Web-based datasets. Relevant topics include, but are not limited to <br>

